-- Ensure pg_trgm extension is available for trigram indexes
CREATE EXTENSION IF NOT EXISTS pg_trgm;

-- ─── Clinical Code Reference Table ─────────────────────────────────
-- Unified reference table for ICD-10, SNOMED, LOINC, RXNORM, and CPT codes.
-- Provides searchable autocomplete data for the pathway editor and patient context forms.

CREATE TABLE IF NOT EXISTS clinical_code_reference (
    code        VARCHAR(20) NOT NULL,
    system      VARCHAR(10) NOT NULL,
    description TEXT NOT NULL,
    category    VARCHAR(100),
    is_common   BOOLEAN NOT NULL DEFAULT false,
    PRIMARY KEY (code, system),
    CHECK (system IN ('ICD-10', 'SNOMED', 'LOINC', 'RXNORM', 'CPT'))
);

CREATE INDEX IF NOT EXISTS idx_ccr_code_trgm ON clinical_code_reference USING gin (code gin_trgm_ops);
CREATE INDEX IF NOT EXISTS idx_ccr_desc_trgm ON clinical_code_reference USING gin (description gin_trgm_ops);
CREATE INDEX IF NOT EXISTS idx_ccr_system ON clinical_code_reference (system);
CREATE INDEX IF NOT EXISTS idx_ccr_common ON clinical_code_reference (system, is_common) WHERE is_common = true;

